/**
 * Generate version constants from hua-ux package.json
 * 
 * 빌드 시점에 hua-ux 패키지의 버전을 읽어서 상수 파일을 생성합니다.
 * 이렇게 하면 npm 배포 후에도 올바른 버전을 사용할 수 있습니다.
 */

import * as fs from 'fs';
import * as path from 'path';

const HUA_UX_PACKAGE_JSON = path.resolve(__dirname, '../../hua-ux/package.json');
const VERSION_OUTPUT = path.resolve(__dirname, '../src/version.ts');

function generateVersionFile(): void {
  // hua-ux 패키지의 package.json 읽기
  if (!fs.existsSync(HUA_UX_PACKAGE_JSON)) {
    console.warn(`⚠️  ${HUA_UX_PACKAGE_JSON} not found. Using default version.`);
    // 기본 버전으로 파일 생성
    const defaultVersion = '0.1.0';
    const content = `/**
 * This file is auto-generated at build time.
 * Do not edit manually.
 * 
 * Generated from: ${HUA_UX_PACKAGE_JSON}
 */

export const HUA_UX_VERSION = '^${defaultVersion}';
`;
    fs.writeFileSync(VERSION_OUTPUT, content, 'utf-8');
    return;
  }

  const huaUxPackage = JSON.parse(fs.readFileSync(HUA_UX_PACKAGE_JSON, 'utf-8'));
  const version = huaUxPackage.version;

  if (!version) {
    throw new Error(`Version not found in ${HUA_UX_PACKAGE_JSON}`);
  }

  // 버전 앞에 ^ 추가 (예: 0.1.0 -> ^0.1.0)
  const versionWithCaret = `^${version}`;

  // TypeScript 파일 생성
  const content = `/**
 * This file is auto-generated at build time.
 * Do not edit manually.
 * 
 * Generated from: ${HUA_UX_PACKAGE_JSON}
 * Source version: ${version}
 */

export const HUA_UX_VERSION = '${versionWithCaret}';
`;

  fs.writeFileSync(VERSION_OUTPUT, content, 'utf-8');
  console.log(`✅ Generated version file: ${VERSION_OUTPUT} (version: ${versionWithCaret})`);
}

// 실행
try {
  generateVersionFile();
} catch (error) {
  console.error('❌ Error generating version file:', error);
  process.exit(1);
}
